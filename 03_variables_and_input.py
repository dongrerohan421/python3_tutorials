'''
This program shows use of variable and inputs
'''

# Variable value
myVariable = 50
print (myVariable)
print (myVariable + 20)


# input convert the user value into string
value = input ("Enter the value: ")
print (value)


# To convert into number use 'int' before input
intValue = int(input("Enter the integer value: "))
print (intValue + 20)


# To convert into decimal use 'float' before input
floatValue = int(input("Enter the decimal value: "))
print (floatValue + 20.21)
